  const applyPostDecryptList = useCallback(
    (
      wireForForward: ArrayBuffer,
      decodedList: DecodedAudioPacket[],
      startedAt: number
    ) => {
      if (decodedList.length === 0) {
        metricsRef.current.recordPacketDropped();
        metricsRef.current.recordIncomingPacketDuration(
          performance.now() - startedAt
        );
        debugWarn(
          '[GCall] decodeAudioPacket failed — size:',
          wireForForward.byteLength
        );
        return;
      }

      const head = decodedList[0]!;
      const { sourceAddr } = head;
      const myAddress = userInfo?.address ?? '';

      const jbExisting = jitterMapRef.current.get(sourceAddr);
      if (!jbExisting) {
        debugLog(
          '[GCall] First packet from',
          sourceAddr.slice(0, 12),
          '— seq:',
          head.seq,
          'vad:',
          head.vad,
          'frames:',
          decodedList.length
        );
      }

      const now = Date.now();
      const maxSpeakers =
        myRoleRef.current === 'root-forwarder'
          ? MAX_ACTIVE_SPEAKERS_GLOBAL
          : MAX_ACTIVE_SPEAKERS_LOCAL;

      const anyVad = decodedList.some((d) => d.vad);
      if (anyVad) {
        lastVadTrueAtRef.current.set(sourceAddr, now);
        localSpeakersRef.current.set(sourceAddr, now);
        lastRecvAtRef.current.set(sourceAddr, performance.now());
      }

      const lastVadTrueAt = lastVadTrueAtRef.current.get(sourceAddr) ?? 0;
      const inForwardHangover =
        !anyVad &&
        lastVadTrueAt > 0 &&
        now - lastVadTrueAt < FORWARD_VAD_HANGOVER_MS;
      const effectiveVadForForward = anyVad || inForwardHangover;
      const isActiveSpeaker = evaluateActiveSpeaker(
        localSpeakersRef.current,
        sourceAddr,
        effectiveVadForForward,
        now,
        maxSpeakers,
        SPEAKER_GATE_WINDOW_MS
      );

      const role = myRoleRef.current;
      const topo = topologyRef.current;

      if ((role === 'cluster-forwarder' || role === 'root-forwarder') && topo) {
        if (isActiveSpeaker) {
          const forwarded = forwardPacketForRole(
            role,
            topo,
            myAddress,
            sourceAddr,
            wireForForward,
            sendPacketToPeerWithRelay
          );
          if (forwarded > 0)
            metricsRef.current.recordPacketForwarded(forwarded);
        }
      }

      if (sourceAddr === myAddress) {
        metricsRef.current.recordIncomingPacketDuration(
          performance.now() - startedAt
        );
        return;
      }

      let jb = jitterMapRef.current.get(sourceAddr);
      if (!jb) {
        jb = new JitterBuffer();
        jitterMapRef.current.set(sourceAddr, jb);
        updateMetricResourceCounts();
      }
      for (const decoded of decodedList) {
        jb.push(decoded.seq, decoded.opusFrame);
      }
      recordInterArrivalForPlayout(sourceAddr);
      if (jb.hasReadyFrame()) {
        activeJitterSourcesRef.current.add(sourceAddr);
      }
      metricsRef.current.recordPacketDecoded(decodedList.length);
      metricsRef.current.recordIncomingPacketDuration(
        performance.now() - startedAt
      );
    },
    [
      recordInterArrivalForPlayout,
      sendPacketToPeerWithRelay,
      updateMetricResourceCounts,
      userInfo?.address,
    ]
  );

