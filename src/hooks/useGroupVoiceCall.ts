/** sha256 → Uint8Array. Used for deterministic election. */
async function sha256Bytes(input: string): Promise<Uint8Array> {
  const enc = new TextEncoder();
  const buf = await crypto.subtle.digest('SHA-256', enc.encode(input));
  return new Uint8Array(buf);
}

/** Compare two Uint8Arrays lexicographically. */
function compareBytes(a: Uint8Array, b: Uint8Array): number {
  for (let i = 0; i < Math.min(a.length, b.length); i++) {
    if (a[i] < b[i]) return -1;
    if (a[i] > b[i]) return 1;
  }
  return a.length - b.length;
}

/** Compute election scores for all participants. Returns sorted list (lowest score first). */
async function computeElectionOrder(
  addresses: string[],
  roomId: string
): Promise<string[]> {
  const scores = await Promise.all(
    addresses.map(async (addr) => ({
      addr,
      score: await sha256Bytes(addr + ':' + roomId),
    }))
  );
  scores.sort((a, b) => compareBytes(a.score, b.score));
  return scores.map((s) => s.addr);
}

/** Build topology from a sorted participant list. */
function buildTopology(sorted: string[], topologyEpoch: number): GroupTopology {
  if (sorted.length <= CLUSTER_SIZE) {
    return {
      topologyEpoch,
      rootForwarder: sorted[0] ?? '',
      standbyForwarder: sorted[1] ?? '',
      clusters: [
        {
          members: sorted,
          forwarder: sorted[0] ?? '',
          standby: sorted[1] ?? '',
        },
      ],
    };
  }

  // Hierarchical: cluster forwarders + root
  const clusters: ClusterDef[] = [];
  for (let i = 0; i < sorted.length; i += CLUSTER_SIZE) {
    const chunk = sorted.slice(i, i + CLUSTER_SIZE);
    clusters.push({
      members: chunk,
      forwarder: chunk[0],
      standby: chunk[1] ?? chunk[0],
    });
  }

  // Root forwarder is the cluster forwarder with the lowest overall score
  const clusterForwarders = clusters.map((c) => c.forwarder);
  const rootForwarder = clusterForwarders[0];
  const standbyForwarder = clusterForwarders[1] ?? clusterForwarders[0];

  return { topologyEpoch, rootForwarder, standbyForwarder, clusters };
}

/** Determine this node's role given a topology. */
function computeMyRole(myAddress: string, topology: GroupTopology): MyRole {
  if (myAddress === topology.rootForwarder) return 'root-forwarder';
  if (myAddress === topology.standbyForwarder) return 'standby-forwarder';
  for (const cluster of topology.clusters) {
    if (myAddress === cluster.forwarder) return 'cluster-forwarder';
  }
  return 'participant';
}

/** Find which cluster forwarder a participant should send to. */
function findAssignedForwarder(
  myAddress: string,
  topology: GroupTopology
): string {
  for (const cluster of topology.clusters) {
    if (cluster.members.includes(myAddress)) {
      return cluster.forwarder;
    }
  }
  return topology.rootForwarder;
}

/** Convert Float32 PCM to Int16 PCM for WebCodecs AudioData. */
function float32ToInt16(f32: Float32Array): Int16Array {
  const i16 = new Int16Array(f32.length);
  for (let i = 0; i < f32.length; i++) {
    i16[i] = Math.max(-32768, Math.min(32767, Math.round(f32[i] * 32767)));
  }
  return i16;
}

/**
 * Sign fields for group call messages.
 * Delegates to the background process (which holds the private key) via
 * the same 'signPresenceMessage' IPC used by useP2PChat and presence.
 * The background performs: sort keys → JSON.stringify → Ed25519 → Base58.
 */
async function signGroupCallFields(
  fields: Record<string, unknown>
): Promise<string> {
  const result = await (window as any).sendMessage(
    'signPresenceMessage',
    fields,
    10_000
  );
  if (result?.error) throw new Error(String(result.error));
  if (typeof result?.signature !== 'string')
    throw new Error('signPresenceMessage returned no signature');
  return result.signature as string;
}

// ── Jitter buffer ──────────────────────────────────────────────────────────────

interface JitterEntry {
  seq: number;
  opusFrame: Uint8Array;
  receivedAt: number;
}

class JitterBuffer {
  private entries: JitterEntry[] = [];
  private lastPlayedSeq = -1;
  private pendingMissedSeq = 0;

  /** Pop-side gap detection: call after pop; returns missed frame count since last pop. */
  consumePendingMissedFrames(): number {
    const m = this.pendingMissedSeq;
    this.pendingMissedSeq = 0;
    return m;
  }

  push(seq: number, opusFrame: Uint8Array): void {
    if (seq <= this.lastPlayedSeq) return; // already played or older
    let insertAt = this.entries.length;
    while (insertAt > 0) {
      const prev = this.entries[insertAt - 1];
      if (prev.seq === seq) return; // duplicate
      if (prev.seq < seq) break;
      insertAt--;
    }
    this.entries.splice(insertAt, 0, {
      seq,
      opusFrame,
      receivedAt: performance.now(),
    });
    const maxEntries = JITTER_BUFFER_SIZE * 2;
    if (this.entries.length > maxEntries) {
      this.entries.splice(0, this.entries.length - maxEntries);
    }
  }

  /** Returns next frame to play, or null if buffer not ready. */
  pop(): Uint8Array | null {
    if (this.entries.length < JITTER_BUFFER_SIZE) return null;
    const entry = this.entries.shift();
    if (!entry) return null;
    if (this.lastPlayedSeq >= 0) {
      const expected = (this.lastPlayedSeq + 1) & 0xffff;
      const gap = (entry.seq - expected + 65536) % 65536;
      if (gap > 0 && gap <= 48) this.pendingMissedSeq += gap;
    }
    this.lastPlayedSeq = entry.seq;
    return entry.opusFrame;
  }

  hasReadyFrame(): boolean {
    return this.entries.length >= JITTER_BUFFER_SIZE;
  }

  clear(): void {
    this.entries = [];
    this.lastPlayedSeq = -1;
    this.pendingMissedSeq = 0;
  }
}

